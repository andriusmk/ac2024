def test_parse():
    data = """########
#..O.O.#
##@.O..#

<^^>>>vv<v>>v<<"""

    state = task.parse(data)
    assert state.commands == "<^^>>>vv<v>>v<<"
    assert state.robot == task.Vector(2, 2)
    assert state.map_size == task.FrozenVector(8, 3)
    assert state.boxes == {
        task.FrozenVector(3, 1),
        task.FrozenVector(5, 1),
        task.FrozenVector(4, 2)
    }
    assert task.FrozenVector(0, 0) in state.walls
    assert task.FrozenVector(7, 0) in state.walls
    assert task.FrozenVector(7, 2) in state.walls
    assert task.FrozenVector(0, 2) in state.walls
    